package cz.hrnr.company_concepts.watson;

import java.net.URL;

public class Credentials {
	private URL url;
	private String apikey;

	public URL getUrl() {
		return url;
	}
	public void setUrl(URL url) {
		this.url = url;
	}
	public String getApikey() {
		return apikey;
	}
	public void setApikey(String apikey) {
		this.apikey = apikey;
	}
}


